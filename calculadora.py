
n1 = int(input("Insira um número: "))
n2 = int(input("Insira outro número: "))

soma = n1 + n2
subtracao = n1 - n2
divisao = n1 / n2
multiplicacao = n1 * n2
modulo = n1 % n2
divisaoExata = n1 // n2

print("A soma dos números é:", soma)
print("A subtração dos números é:", subtracao)
print("A divisão dos números é:", divisao)
print("A multiplicação dos números é:", multiplicacao)
print("O módulo dos números é:", modulo)
print("A divisão exata dos números é:", divisaoExata)
